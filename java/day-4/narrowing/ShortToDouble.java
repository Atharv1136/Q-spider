public class ShortToDouble {
    public static void main(String[] args) {
        short s = 30000;
        double d = s;
        System.out.println("Short: " + s);
        System.out.println("Double: " + d);
        
        short s2 = -15000;
        double d2 = s2;
        System.out.println("Short: " + s2);
        System.out.println("Double: " + d2);
    }
}