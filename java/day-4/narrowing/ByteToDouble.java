public class ByteToDouble {
    public static void main(String[] args) {
        byte b = 100;
        double d = b;
        System.out.println("Byte: " + b);
        System.out.println("Double: " + d);
        
        byte b2 = -50;
        double d2 = b2;
        System.out.println("Byte: " + b2);
        System.out.println("Double: " + d2);
    }
}