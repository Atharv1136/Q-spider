public class task_short {
    public static void main(String[] args) {
        short s1 = 20;
        System.out.println(s1);

        char c1 = (char) s1;
        System.out.println(c1); // no output

        int i1 = s1;
        System.out.println(i1);

        long l1 = s1;
        System.out.println(l1);

        float f1 = s1;
        System.out.println(f1);

        double d1 = s1;
        System.out.println(d1);
    }
}
